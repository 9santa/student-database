def generate_name():
    if random.random() < 0.5:
        return f"{random.choice(last_names_m)} {random.choice(first_names_m)}"
    else:
        return f"{random.choice(last_names_f)} {random.choice(first_names_f)}"

def generate_group_code():
    prefix = random.choice(prefixes)
    num = randint(100, 999)
    return f"{prefix}-{num}"

def generate_year():
    return randint(2010, 2025)

def generate_students():
    return randint(8, 99)

# Генерация 1000 записей
records = []
for _ in range(1000):
    code = generate_group_code()
    year = generate_year()
    degree = random.choice(degrees)
    faculty = random.choice(faculties)
    captain = generate_name()
    students = generate_students()
    
    records.append(f"{code},{year},{degree},{faculty},{captain},{students}")

